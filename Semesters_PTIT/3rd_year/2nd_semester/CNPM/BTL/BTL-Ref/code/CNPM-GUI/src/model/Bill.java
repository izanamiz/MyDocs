package model;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 *
 * @author ngahn
 */
public class Bill implements Serializable {

    private String id;
    private Timestamp time;
    private User user;

    public Bill() {
        super();
    }

    public Bill(String id, Timestamp time, User user) {
        this.id = id;
        this.time = time;
        this.user = user;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Timestamp getTime() {
        return time;
    }

    public void setTime(Timestamp time) {
        this.time = time;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

}
